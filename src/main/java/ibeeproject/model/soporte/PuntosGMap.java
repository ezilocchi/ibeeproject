package ibeeproject.model.soporte;

/**
 *
 * @author farias.facundo 
 */
public class PuntosGMap {

    private int numeroApiar;
    private String denominacion;
    private String estado;
    private double latitud;
    private double longitud;

    /**
     * @return the numeroApiar
     */
    public int getNumeroApiar() {
        return numeroApiar;
    }

    /**
     * @param numeroApiar the numeroApiar to set
     */
    public void setNumeroApiar(int numeroApiar) {
        this.numeroApiar = numeroApiar;
    }

    /**
     * @return the denominacion
     */
    public String getDenominacion() {
        return denominacion;
    }

    /**
     * @param denominacion the denominacion to set
     */
    public void setDenominacion(String denominacion) {
        this.denominacion = denominacion;
    }

    /**
     * @return the estado
     */
    public String getEstado() {
        return estado;
    }

    /**
     * @param estado the estado to set
     */
    public void setEstado(String estado) {
        this.estado = estado;
    }

    /**
     * @return the latitud
     */
    public double getLatitud() {
        return latitud;
    }

    /**
     * @param latitud the latitud to set
     */
    public void setLatitud(double latitud) {
        this.latitud = latitud;
    }

    /**
     * @return the longitud
     */
    public double getLongitud() {
        return longitud;
    }

    /**
     * @param longitud the longitud to set
     */
    public void setLongitud(double longitud) {
        this.longitud = longitud;
    }


}
