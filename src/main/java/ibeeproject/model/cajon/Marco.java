package ibeeproject.model.cajon;

/**
 *
 * @author Fede 
 */
public class Marco {

    private int nroMarco;
    private int nroCajon;
    private int nroAlza;
    private String descripcion;
    private String tamaño;
    private String observaciones;

    /**
     * @return the nroMarco
     */
    public int getNroMarco() {
        return nroMarco;
    }

    /**
     * @param nroMarco the nroMarco to set
     */
    public void setNroMarco(int nroMarco) {
        this.nroMarco = nroMarco;
    }

    /**
     * @return the nroCajon
     */
    public int getNroCajon() {
        return nroCajon;
    }

    /**
     * @param nroCajon the nroCajon to set
     */
    public void setNroCajon(int nroCajon) {
        this.nroCajon = nroCajon;
    }

    /**
     * @return the nroAlza
     */
    public int getNroAlza() {
        return nroAlza;
    }

    /**
     * @param nroAlza the nroAlza to set
     */
    public void setNroAlza(int nroAlza) {
        this.nroAlza = nroAlza;
    }

    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the tamaño
     */
    public String getTamaño() {
        return tamaño;
    }

    /**
     * @param tamaño the tamaño to set
     */
    public void setTamaño(String tamaño) {
        this.tamaño = tamaño;
    }

    /**
     * @return the observaciones
     */
    public String getObservaciones() {
        return observaciones;
    }

    /**
     * @param observaciones the observaciones to set
     */
    public void setObservaciones(String observaciones) {
        this.observaciones = observaciones;
    }

}
